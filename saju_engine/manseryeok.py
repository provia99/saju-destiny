_MONTH_TO_IDX = {1:0, 2:1, 3:2, 4:3, 5:4, 6:5, 7:6, 8:7, 9:8, 10:9, 11:10, 12:11}

def _get_jeolyip_day(year: int, month: int) -> int:
    """해당 연도·월의 절기 입기일(日)을 반환. 테이블 범위 밖이면 근사값 사용."""
    row = _JEOLYIP_TABLE.get(year)
    if row is not None:
        return row[_MONTH_TO_IDX[month]]
    # 테이블 범위 밖 (2101+): 근사값
    approx = {1:5, 2:4, 3:5, 4:5, 5:5, 6:5, 7:7, 8:7, 9:7, 10:8, 11:7, 12:7}
    return approx[month]

class Manseryeok:
    """만세력 계산기"""
    
    # 기준일: 1900년 1월 1일 = 甲戌일
    BASE_DATE = datetime(1900, 1, 1)
    BASE_DAY_INDEX = 10  # 甲戌 in 60갑자
    
    def __init__(self):
        pass
    
    def calculate_year_pillar(self, year: int, month: int, day: int) -> Tuple[str, str]:
        """
        년주(年柱) 계산
        
        입춘(立春) 이전이면 전년도의 간지 사용
        
        Args:
            year: 양력 년
            month: 양력 월
            day: 양력 일
            
        Returns:
            (천간, 지지)
        """
        current_date = datetime(year, month, day)
        lichun = get_lichun_date(year)
        
        # 입춘 이전이면 전년도 간지
        if current_date < lichun:
            year -= 1
        
        # 1864년 = 갑자년 (60갑자의 시작)
        # 년주는 1864년부터 계산
        year_index = (year - 1864) % 60
        
        return get_gapja_by_index(year_index)
    
    def calculate_month_pillar(self, year: int, month: int, day: int) -> Tuple[str, str]:
        """
        월주(月柱) 계산
        
        절입(節入) 기준으로 월 변경
        입춘-경칩-청명-입하-망종-소서-입추-백로-한로-입동-대설-소한
        
        Args:
            year: 양력 년
            month: 양력 월
            day: 양력 일
            
        Returns:
            (천간, 지지)
        """
        # 절기별 월지 매핑
        # 입춘(2월) → 寅월, 경칩(3월) → 卯월, ...
        month_branches = {
            2: '寅',   # 입춘
            3: '卯',   # 경칩
            4: '辰',   # 청명
            5: '巳',   # 입하
            6: '午',   # 망종
            7: '未',   # 소서
            8: '申',   # 입추
            9: '酉',   # 백로
            10: '戌',  # 한로
            11: '亥',  # 입동
            12: '子',  # 대설
            1: '丑'    # 소한
        }

        # 절기 입기일: _JEOLYIP_TABLE 기반 정확 날짜 사용
        jeolyip_day = _get_jeolyip_day(year, month)

        # 절입일 이전이면 전월의 월지 사용
        if day < jeolyip_day:
            month -= 1
            if month == 0:
                month = 12
        
        month_branch = month_branches.get(month, '寅')
        
        # 월간(月干) 계산: 년간에 따라 결정 (오호판두법)
        # 甲己년: 丙寅월부터 시작
        # 乙庚년: 戊寅월부터 시작
        # 丙辛년: 庚寅월부터 시작
        # 丁壬년: 壬寅월부터 시작
        # 戊癸년: 甲寅월부터 시작
        
        year_stem, _ = self.calculate_year_pillar(year, month, day)
        
        year_stem_to_first_month_stem = {
            '甲': 2, '己': 2,   # 丙
            '乙': 4, '庚': 4,   # 戊
            '丙': 6, '辛': 6,   # 庚
            '丁': 8, '壬': 8,   # 壬
            '戊': 0, '癸': 0    # 甲
        }
        
        base_stem_index = year_stem_to_first_month_stem.get(year_stem, 0)
        
        # 寅월부터 시작하므로 寅=0, 卯=1, 辰=2, ...
        month_branch_index = EARTHLY_BRANCHES.index(month_branch)
        # 寅을 기준(0)으로 맞추기
        yin_index = EARTHLY_BRANCHES.index('寅')
        month_offset = (month_branch_index - yin_index) % 12
        
        month_stem_index = (base_stem_index + month_offset) % 10
        month_stem = HEAVENLY_STEMS[month_stem_index]
        
        return (month_stem, month_branch)
    
    def calculate_day_pillar(self, year: int, month: int, day: int) -> Tuple[str, str]:
        """
        일주(日柱) 계산
        
        기준일(1900-01-01 = 甲辰)로부터 경과일수로 계산
        
        Args:
            year: 양력 년
            month: 양력 월
            day: 양력 일
            
        Returns:
            (천간, 지지)
        """
        target_date = datetime(year, month, day)
        delta = target_date - self.BASE_DATE
        days_passed = delta.days
        
        # 60갑자 순환
        day_index = (self.BASE_DAY_INDEX + days_passed) % 60
        
        return get_gapja_by_index(day_index)
    
    def calculate_time_pillar(self, year: int, month: int, day: int, hour: int, minute: int = 0) -> Tuple[str, str]:
        """
        시주(時柱) 계산

        정자시(正子時) 기준 적용:
        - 23:30 이후 (23시 30분~): 다음 날 조자시(早子時) → 다음 날 일간 기준
          (JS 엔진 saju_calc.js의 총분>=1410 기준과 동일)
        - 23:00~23:29: 해시(亥時) 연장, 당일 일간 사용
        - 00:00~01:00: 조자시(早子時), 당일 일간 사용

        Args:
            year: 양력 년
            month: 양력 월
            day: 양력 일
            hour: 시간 (0-23)
            minute: 분 (0-59, 기본값 0)

        Returns:
            (천간, 지지)
        """
        # 정자시 판정: 23:30 이후 → 다음 날 일간 사용 (자시 전반 = 다음날)
        if hour == 23 and minute >= 30:
            current_date = datetime(year, month, day)
            next_date = current_date + timedelta(days=1)
            day_stem, _ = self.calculate_day_pillar(
                next_date.year,
                next_date.month,
                next_date.day
            )
        else:
            # 23:00~23:29 및 기타 시간: 당일의 일간 사용
            day_stem, _ = self.calculate_day_pillar(year, month, day)

        # 시지 구하기
        time_branch = get_time_branch(hour)

        # 시간 구하기 (오자원두법)
        time_stem = get_time_stem(day_stem, time_branch)

        return (time_stem, time_branch)
    
    def calculate_four_pillars(
        self,
        year: int,
        month: int,
        day: int,
        hour: int,
        minute: int = 0,
        use_yajojasi: bool = True
    ) -> dict:
        """
        사주 4주 8자 전체 계산

        정자시(正子時) 기준 적용 (use_yajojasi=True일 때):
        - 23:30 이후: 다음 날 조자시 → 일주는 다음 날 기준
        - 23:00~23:29: 해시(亥時) 연장, 당일 일주 사용
        - 00:00~01:00: 조자시, 당일 일주 사용

        Args:
            year: 양력 년
            month: 양력 월
            day: 양력 일
            hour: 시간 (0-23)
            minute: 분 (0-59, 기본값 0)
            use_yajojasi: 정자시 규칙 적용 여부 (기본: True)

        Returns:
            {
                'year': (년간, 년지),
                'month': (월간, 월지),
                'day': (일간, 일지),
                'time': (시간, 시지)
            }
        """
        # 정자시(23:30+) 처리: use_yajojasi가 True일 때만 적용
        if use_yajojasi and hour == 23 and minute >= 30:
            current_date = datetime(year, month, day)
            next_date = current_date + timedelta(days=1)

            # 년주·월주는 당일 기준
            year_pillar = self.calculate_year_pillar(year, month, day)
            month_pillar = self.calculate_month_pillar(year, month, day)

            # 일주는 다음 날 기준 (자시 전반 = 다음날)
            day_pillar = self.calculate_day_pillar(
                next_date.year,
                next_date.month,
                next_date.day
            )
        else:
            # 그 외 모든 시간: 당일 기준
            year_pillar = self.calculate_year_pillar(year, month, day)
            month_pillar = self.calculate_month_pillar(year, month, day)
            day_pillar = self.calculate_day_pillar(year, month, day)

        # 시주 계산
        time_pillar = self.calculate_time_pillar(year, month, day, hour, minute)
        
        return {
            'year': year_pillar,
            'month': month_pillar,
            'day': day_pillar,
            'time': time_pillar
        }

