  // 격국/재물
  재물구조: 격국재물[격짧] || '용신 방향에서 자연스럽게 흐르는',
  재물주의: `기신 ${결과.기신}(${oh[결과.기신]}) 방향의 투자·동업은 신중하십시오.`,
  // 합충형
  합충형요약: 합충형요약,
  // 신살
  길신요약: 길신요약.join(' / ') || '없음',
  흉살요약: 흉살요약.join(' / ') || '없음',
  공망요약: 공망요약 || '공망 없음',
  // 십성
  있는십성: 있는십성,
  // 일지 배우자
