  const cd = 결과.현재대운;
  현재대운간지 = 주표기(cd.천간, cd.지지);
  현재대운나이범위 = `${cd.시작나이}~${cd.종료나이}세`;
  const 길흉 = cd.길흉 || cd.대운길흉 || '';
  현재대운해석 = /용신/.test(길흉) ? '용신 기운이 함께하는 시기입니다. 적극적으로 움직이십시오.'
    : /희신/.test(길흉) ? '희신 기운이 보조하는 시기입니다. 안정적으로 추진하십시오.'
    : /기신/.test(길흉) ? '기신 기운이 작용하는 시기입니다. 방어적으로 임하십시오.'
    : '중립적 흐름입니다. 용신 방향으로 에너지를 집중하십시오.';
