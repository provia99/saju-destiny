const 격국재물 = {
  정재격:'꾸준하고 안정적인 축적', 편재격:'역동적이고 유동적인 흐름',
  정관격:'조직 안에서의 안정 수입', 편관격:'도전과 경쟁 속 성취',
  식신격:'재능과 기술 기반 수입', 상관격:'창의적 표현 기반 수입',
  정인격:'학문·교육·신뢰 기반', 편인격:'전문 기술·특수 분야',
  건록격:'자수성가·독립형', 겁재격:'경쟁·파트너십 기반',
};

// ── 합충형 요약 ──
const 합충 = 결과.합충형파해 || {};
let 합충형요약 = '';
if (합충.천간합?.length) 합충형요약 += `천간합: ${합충.천간합.map(h=>`${h.간1}${h.간2} → ${h.합화}(${oh[h.합화]})`).join(', ')}\n`;
