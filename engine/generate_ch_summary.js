const 건강맵 = {木:'간·담·근육·눈',火:'심장·소장·혈액순환',土:'위장·비장·소화기',金:'폐·대장·피부·호흡기',水:'신장·방광·뼈·귀'};
const 띠맵 = 지지띠; // saju_calc에서 import

const ilgan = 결과.일간;
const _설명 = 일간설명[ilgan] || 일간설명['甲'];
const 원국 = 결과.원국;

function 주표기(천, 지) { return `${천}${지}(${천간음[천]}${지지음[지]})`; }

// ── 나이대 (saju_calc 결과 직접 사용) ──
const 만나이 = 결과.만나이 || 0;
const 나이대 = 결과.나이대 || '30대';

// ── 오행 등급 (saju_calc 기준 — 총본과 동일) ──
const 오행등급 = {};
for (const [k,v] of Object.entries(결과.오행점수)) {
  오행등급[k] = _오행등급fn(v);
}

// ── 천간 10종 요약 생성 ──
const 천간목록 = ['甲','乙','丙','丁','戊','己','庚','辛','壬','癸'];
const 천간10종요약 = 천간목록.map(g => {
  const d = 일간설명[g];
  const 나 = g === ilgan ? '  ← {{이름}} 님이 바로 이 사람!' : '';
  return `${g}(${천간음[g]}) — ${d.비유}${나}\n  ${d.한줄소개.substring(0,50)}...`;
}).join('\n\n');

// ── 대운 요약 (saju_calc 길흉 판정 사용 — 총본과 동일) ──
const _길흉아이콘 = {'용신대운':'🟢 용신대운','희신대운':'🔵 희신대운','기신대운':'🔴 기신대운','중립대운':'⚪ 중립대운'};
const 대운요약 = 결과.대운목록.map(d => {
  const 길흉 = d.길흉 || d.대운길흉 || '중립대운';
  const 등급 = _길흉아이콘[길흉] || `⚪ ${길흉}`;
  const 현재 = (d.시작나이 <= 만나이 && d.시작나이+9 >= 만나이) ? ' ◀ 지금!' : '';
  return `  ${d.천간}${d.지지}(${천간음[d.천간]}${지지음[d.지지]}) ${d.시작나이}~${d.시작나이+9}세 ${등급}${현재}`;
}).join('\n');

// ── 현재 대운 (saju_calc 길흉 사용) ──
