if (!합충형요약) 합충형요약 = '특별한 합충형이 없습니다. 안정적인 구조입니다.';

// ── 신살 요약 ──
const 신살 = 결과.신살 || {};
let 길신요약 = [];
let 흉살요약 = [];
if ((신살.천을귀인||[]).length) 길신요약.push(`천을귀인(天乙貴人) ${신살.천을귀인.join('·')}에 위치`);
