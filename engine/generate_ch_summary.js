  용신직업군: 직업맵[결과.용신] || '',
  기신건강: 건강맵[결과.기신] || '',
  일간건강: 건강맵[결과.일간오행] || '',
  // 일간 설명 슬롯
  일간설명_비유: _설명.비유,
