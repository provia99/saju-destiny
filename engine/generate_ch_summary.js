  월주: 주표기(원국.월주.천간, 원국.월주.지지),
  시주: 주표기(원국.시주.천간, 원국.시주.지지),
  용신: 결과.용신,
  용신오행: `${결과.용신}(${oh[결과.용신]})`,
  희신오행: `${결과.희신}(${oh[결과.희신]})`,
  기신오행: `${결과.기신}(${oh[결과.기신]})`,
  구신오행: `${결과.구신}(${oh[결과.구신]})`,
