const 오행색상 = {木:'초록·연두',火:'적색·주황',土:'황토·베이지',金:'흰색·은색·금색',水:'검정·남색'};
const 오행방위 = {木:'동(東)',火:'남(南)',土:'중앙',金:'서(西)',水:'북(北)'};
