  건강주의1순위: 건강맵[결과.기신] || '',
  // 누락 슬롯 보충
  용신음식: {木:'신맛·녹색채소·시금치·부추',火:'쓴맛·붉은식품·토마토·고추',土:'단맛·곡물·감자·고구마·꿀',金:'짠맛·흰색식품·두부·배·해산물',水:'짠맛·검은콩·해조류·미역'}[결과.용신] || '',
  현재대운해석요약: 현재대운해석,
  지장간요약: '일지 지장간에 숨겨진 에너지가 있습니다. 나이가 들수록 이 숨은 기운이 드러납니다.',
  재물전략: `저축 30% 이상 유지 / 안정 자산 중심 / 용신 방향 달에 중요 결정`,
  재물구조요약: 격국재물[격짧] || '용신 방향에서 자연스럽게 흐르는',
  의사결정스타일: 결과.신강약.includes('신강') ? '독립적 판단이 강합니다. 자기 기준이 뚜렷합니다.' : '주변 의견을 참고하는 것이 효과적입니다. 협력 속에서 결정하십시오.',
  올해직업전략: `용신 ${결과.용신}(${oh[결과.용신]}) 방향 업무에 집중하십시오`,
  올해좋은달: '용신 방향 월 (달력 참고)',
