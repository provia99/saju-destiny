function 추출오행키(v) {
  if (!v) return null;
  const m = {木:'木',火:'火',土:'土',金:'金',水:'水',목:'木',화:'火',토:'土',금:'金',수:'水'};
  if (m[v]) return m[v];
  const first = String(v).charAt(0);
  return m[first] || null;
}
function 오행텍스트(oh) {
  const t = {木:'목(木)',火:'화(火)',土:'토(土)',金:'금(金)',水:'수(水)'};
  return t[oh] || oh || '';
}

