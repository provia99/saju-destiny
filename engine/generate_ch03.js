function normalizeShingang(raw) {
  if(!raw) return '';
  if(raw.includes('(')) return raw;
  if(raw.includes('신강')) return '신강(身强)';
  if(raw.includes('신약')) return '신약(身弱)';
  if(raw.includes('중화')) return '중화(中和)';
  return raw;
}
