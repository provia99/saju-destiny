// 주별 한자 표기 생성 함수
function 주표기(천간, 지지) {
  return `${천간}${지지}(${천간음[천간]}${지지음[지지]})`;
}

// 천간합 목록 문자열 생성
// 표기 규칙: 두 글자 조합(한글음) + 한자 용어 → 癸戊(계무)합
function 천간합목록() {
  if (!합.천간합 || 합.천간합.length === 0) return '없음';
  return 합.천간합.map(h =>
    `${h.간1}${h.간2}(${천간음[h.간1]}${천간음[h.간2]})합, 합화 ${h.합화}(${오행명[h.합화]}), ${h.위치1}·${h.위치2}`
  ).join(' / ');
}

