function get관성해석(십성명, 성별) {
  if (성별 === '여성') return 관성_여성[십성명] || null;
  return 관성_남성[십성명] || null;
}

function get재성해석(십성명, 성별) {
  if (성별 === '여성') return 재성_여성[십성명] || null;
  return 재성_남성[십성명] || null;
}

function get인성해석(십성명) {
  return 인성_공통[십성명] || null;
}

// ── 슬롯에 성별 해석 텍스트 주입 ────────────────────────────────
// M.재성종류, M.관성종류, M.성별 기준으로 해석 텍스트를 M에 주입
function injectGenderHaeseok(M) {
  const 성별 = M.성별 || '남성';

  // 관성 해석
  const 관성명 = (M.관성종류 || '').replace(/\([^)]*\)/g,'').trim().toLowerCase();
  const 관성키 = 관성명.includes('편관') ? '편관' : 관성명.includes('정관') ? '정관' : null;
  if (관성키) {
    const 텍스트 = get관성해석(관성키, 성별);
    if (텍스트) M._관성성별해석 = 텍스트;
  }

  // 재성 해석
  const 재성명 = (M.재성종류 || '').replace(/\([^)]*\)/g,'').trim().toLowerCase();
  const 재성키 = 재성명.includes('편재') ? '편재' : 재성명.includes('정재') ? '정재' : null;
  if (재성키) {
    const 텍스트 = get재성해석(재성키, 성별);
    if (텍스트) M._재성성별해석 = 텍스트;
  }

  // 인성 해석
  const 인성관련 = ['편인','정인'].find(k => (M.있는십성목록||'').includes(k));
  if (인성관련) {
    const 텍스트 = get인성해석(인성관련);
    if (텍스트) M._인성해석 = 텍스트;
  }

  return M;
}

