// 통합 직업 적합도 계산 함수 (모든 가중치 적용)
// ================================================================
function 직업적합도계산_v2(params) {
  const {
    용신오행, 희신오행, 일간오행, 십성목록,
    신강약단, 일지12운성, 신살목록, 격국명,
    없는십성목록, 공망오행목록,
  } = params;

  const 결과 = {};

  for (const [직업, 오행맵] of Object.entries(오행직업맵)) {
    const 십성맵 = 십성직업맵[직업] || {};

    // 기본 점수 (기존 방식)
    const 용신점수 = (오행맵[용신오행] || 0) * 2.0;
    const 희신점수 = (오행맵[희신오행] || 0) * 1.0;
    const 일간점수 = (오행맵[일간오행] || 0) * 0.5;
    const 오행합계 = 용신점수 + 희신점수 + 일간점수;

    let 십성합계 = 0, 십성카운트 = 0;
    for (const 십성 of (십성목록||[])) {
      const 점수 = 십성맵[십성];
      if (점수 !== undefined) { 십성합계 += 점수; 십성카운트++; }
    }
    const 십성평균 = 십성카운트 > 0 ? 십성합계 / 십성카운트 : 1;

    let 점수 = 오행합계 * 십성평균;

    // 신강약 보정
    const 신강약보정맵 = 신강약직업보정[신강약단] || {};
    점수 *= (신강약보정맵[직업] || 1.0);

    // ① 일지 12운성 보정
    if (일지12운성) {
      const 운성맵 = 십이운성직업보정[일지12운성] || {};
      점수 *= (운성맵[직업] || 1.0);
    }

    // ② 신살 보정 (가산)
    for (const 신살 of (신살목록||[])) {
      const 신살맵 = 신살직업보정[신살] || {};
      점수 *= (신살맵[직업] || 1.0);
    }

    // ③ 격국 보정
    if (격국명) {
      // 격국명에서 핵심 키 추출 (정재격, 편재격 등)
      const 격국키 = Object.keys(격국직업보정).find(k => 격국명.includes(k.replace('(正財格)','').replace('(偏財格)','')));
      const 격맵 = 격국직업보정[격국명] || {};
      점수 *= (격맵[직업] || 1.0);
    }

    // ④ 없는 십성 감산
    for (const 없는십성 of (없는십성목록||[])) {
      const 감산맵 = 없는십성직업감산[없는십성] || {};
      점수 *= (감산맵[직업] || 1.0);
    }

    // ⑤ 공망 오행 감산
    for (const 공망오행 of (공망오행목록||[])) {
      const 공망맵 = 공망직업감산[공망오행] || {};
      점수 *= (공망맵[직업] || 1.0);
    }

    결과[직업] = Math.round(점수 * 10) / 10;
  }

  return Object.entries(결과)
    .sort((a, b) => b[1] - a[1])
    .map(([직업, 점수]) => ({ 직업, 점수 }));
}

