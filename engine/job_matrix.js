// ── 직업 적합도 계산 함수 ───────────────────────────────────────
function 직업적합도계산(용신오행, 희신오행, 일간오행, 십성목록, 신강약단) {
  const 결과 = {};

  for (const [직업, 오행맵] of Object.entries(오행직업맵)) {
    const 십성맵 = 십성직업맵[직업] || {};

    // 1. 오행 점수 (용신 ×2 + 희신 ×1 + 일간 ×0.5)
    const 용신점수 = (오행맵[용신오행] || 0) * 2.0;
    const 희신점수 = (오행맵[희신오행] || 0) * 1.0;
    const 일간점수 = (오행맵[일간오행] || 0) * 0.5;
    const 오행합계 = 용신점수 + 희신점수 + 일간점수;

    // 2. 십성 점수 (있는 십성들의 평균)
    let 십성합계 = 0, 십성카운트 = 0;
    for (const 십성 of 십성목록) {
      const 점수 = 십성맵[십성];
      if (점수 !== undefined) {
        십성합계 += 점수;
        십성카운트++;
      }
    }
    const 십성평균 = 십성카운트 > 0 ? 십성합계 / 십성카운트 : 1;

    // 3. 기본 점수 = 오행합계 × 십성평균
    let 기본점수 = 오행합계 * 십성평균;

    // 4. 신강약 보정
    const 보정맵 = 신강약직업보정[신강약단] || {};
    const 보정배율 = 보정맵[직업] || 1.0;
    const 최종점수 = Math.round(기본점수 * 보정배율 * 10) / 10;

    결과[직업] = 최종점수;
  }

  // 점수 내림차순 정렬
  return Object.entries(결과)
    .sort((a, b) => b[1] - a[1])
    .map(([직업, 점수]) => ({ 직업, 점수 }));
}

