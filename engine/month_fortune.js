function 총운생성(결과, 기준년도, 월운풀이결과) {
  const 세운 = 결과.현재세운 || {};
  const 세운천간 = 세운.천간 || '', 세운지지 = 세운.지지 || '';
  const 세운간지 = `${세운천간}${세운지지}(${천간음[세운천간]||''}${지지음[세운지지]||''})`;
  const 세운천간오행 = 천간오행[세운천간] || '', 세운지지오행 = 지지오행[세운지지] || '';
  const 길흉 = 결과.현재세운길흉 || '';
  const 용신 = 결과.용신, 희신 = 결과.희신, 기신 = 결과.기신;
  const 현재대운 = 결과.현재대운 || {};
  const 대운간지 = `${현재대운.천간||''}${현재대운.지지||''}`;
  const 대운나이 = 현재대운.나이범위 || '';
  const 대운길흉 = 현재대운.대운길흉 || '';
  const 세운십성 = 십성계산(결과.일간, 세운천간) || '';

  // 신강약 분류
  const _신강약 = 결과.신강약 || '';
  let 신강약키 = '신약';
  if (_신강약.includes('극신강')) 신강약키 = '극신강';
  else if (_신강약.includes('극신약')) 신강약키 = '극신약';
  else if (_신강약.includes('신강') || _신강약.includes('중화형')) 신강약키 = '신강';

  const lines = [];

  // ── 제목 ──
  lines.push(`✦ ${결과.이름} 님의 ${기준년도}년 총운`);
  lines.push('');

  // ── 기본 정보 ──
  lines.push(`올해 세운: ${세운간지}`);
  lines.push(`세운 오행: 천간 ${oh[세운천간오행]||''}(${세운천간오행}), 지지 ${oh[세운지지오행]||''}(${세운지지오행})`);
  lines.push(`올해 십성: ${세운십성} — ${십성영역[세운십성]?.키워드||''}`);
  lines.push(`현재 대운: ${대운간지}(${대운나이})`);
  lines.push('');

  // ── 올해의 큰 흐름 ──
  const st = 십성텍스트(세운십성, /용신/.test(길흉)?'용신월':/희신/.test(길흉)?'희신월':/기신/.test(길흉)?'기신월':'중립월');

  // 올해 큰 흐름 (DB 참조)
  const 세운등급 = /용신/.test(길흉)?'용신':/희신/.test(길흉)?'희신':/기신/.test(길흉)?'기신':'중립';
  const 큰흐름 = 총운큰흐름[세운등급];
  lines.push(`◎ 올해의 큰 흐름: ${큰흐름.제목}`);
  lines.push('');
  const 오행표기 = {용신:`${oh[용신]}(${용신})`,희신:`${oh[희신]}(${희신})`,기신:`${oh[기신]}(${기신})`,중립:`${oh[용신]}(${용신})`};
  lines.push(`${기준년도}년은 ${결과.이름} 님에게 ${세운등급==='용신'?`가장 필요한 ${오행표기.용신} 기운이 세운으로 들어오는 해입니다.`:세운등급==='희신'?`희신 ${오행표기.희신}의 힘이 돕는 순탄한 해입니다.`:세운등급==='기신'?`기신 ${오행표기.기신} 기운이 작용하는 해입니다.`:`본인의 노력이 결과를 좌우하는 해입니다.`}`);
  큰흐름.본문.forEach(p => lines.push(p));

  // ── 올해의 주요 테마 (십성 4대 영역) ──
  if (st) {
    lines.push('');
    lines.push(`◎ 올해의 주요 테마: ${세운십성}(${십성영역[세운십성]?.영역||''})`);
    lines.push('');
    if (st.종합) lines.push(st.종합);
    lines.push('');
    if (st.재물) lines.push(`▸ 올해 재물운: ${st.재물}`);
    if (st.직업) lines.push(`▸ 올해 직업운: ${st.직업}`);
    if (st.건강) lines.push(`▸ 올해 건강운: ${st.건강}`);
    if (st.인간관계) lines.push(`▸ 올해 인간관계: ${st.인간관계}`);
  }

  // ── 대운과의 관계 ──
  lines.push('');
  lines.push(`◎ 대운(大運)과의 관계`);
  lines.push(`현재 ${결과.이름} 님은 ${대운간지} 대운(${대운나이}) 구간을 지나고 있습니다.`);
  // 대운 맥락 (DB 참조)
  const 대운키 = /용신/.test(대운길흉) ? (/용신/.test(길흉)?'용신_용신':/기신/.test(길흉)?'용신_기신':'용신_기타')
    : /기신/.test(대운길흉) ? (/기신/.test(길흉)?'기신_기신':/용신/.test(길흉)?'기신_용신':'기신_기타')
    : '기타';
  lines.push(대운맥락DB[대운키] || 대운맥락DB['기타']);

  // ── 신강약 맞춤 올해 조언 (DB 참조) ──
  lines.push('');
  lines.push(`◎ ${결과.이름} 님의 체질에 맞는 올해 전략`);
  const 신강약문장 = 신강약총운[세운등급]?.[신강약키] || '';
  if (신강약문장) lines.push(신강약문장);

  // ── 합충 특수 이벤트 ──
  if (결과.원국) {
    const 충맵 = {子:'午',午:'子',丑:'未',未:'丑',寅:'申',申:'寅',卯:'酉',酉:'卯',辰:'戌',戌:'辰',巳:'亥',亥:'巳'};
    const 합맵 = {子:'丑',丑:'子',寅:'亥',亥:'寅',卯:'戌',戌:'卯',辰:'酉',酉:'辰',巳:'申',申:'巳',午:'未',未:'午'};
    const 위치 = ['년지','월지','일지','시지'];
    const 영역맵 = {년지:'조상·사회',월지:'직업·형제',일지:'배우자·내면',시지:'자녀·말년'};
    const 지지들 = [결과.원국.년주.지지, 결과.원국.월주.지지, 결과.원국.일주.지지, 결과.원국.시주.지지];
    const 합충이벤트 = [];
    for (let i = 0; i < 지지들.length; i++) {
      if (충맵[세운지지] === 지지들[i])
        합충이벤트.push(`⚡ 올해 세운 지지(${세운지지})가 원국 ${위치[i]}(${지지들[i]})와 충(沖)합니다. ${영역맵[위치[i]]} 영역에서 변동이 예상됩니다. 이동, 이직, 관계 변화가 생길 수 있으니 대비하십시오.`);
      if (합맵[세운지지] === 지지들[i])
        합충이벤트.push(`🤝 올해 세운 지지(${세운지지})가 원국 ${위치[i]}(${지지들[i]})와 합(合)합니다. ${영역맵[위치[i]]} 영역에서 좋은 인연, 협력, 결속이 기대됩니다.`);
    }
    if (합충이벤트.length) { lines.push(''); 합충이벤트.forEach(s => lines.push(s)); }
  }

  // ── 분기별 전망 + 최고/최악 달 미리보기 ──
  if (월운풀이결과 && 월운풀이결과.length > 0) {
    lines.push('');
    lines.push(`◎ ${기준년도}년 분기별 전망`);
    lines.push('');

    const 분기 = [
      { 이름:'1분기 (2~4월, 봄)', 월들:[2,3,4] },
      { 이름:'2분기 (5~7월, 여름)', 월들:[5,6,7] },
      { 이름:'3분기 (8~10월, 가을)', 월들:[8,9,10] },
      { 이름:'4분기 (11~1월, 겨울)', 월들:[11,12,1] },
    ];
    for (const q of 분기) {
      const 월들 = 월운풀이결과.filter(p => q.월들.includes(p.월));
      const 용 = 월들.filter(p => p.성격==='용신월').length;
      const 희 = 월들.filter(p => p.성격==='희신월').length;
      const 기 = 월들.filter(p => p.성격==='기신월').length;
      const 등급요약 = 월들.map(p => {
        const 아이콘 = {용신월:'🟢',희신월:'🔵',기신월:'🔴',중립월:'⚪'};
        return `${p.월}월${아이콘[p.성격]}`;
      }).join(' ');

      let 분기판정 = '';
      if (용 >= 2) 분기판정 = '올해 가장 좋은 분기입니다. 이 시기에 핵심 계획을 집중 실행하십시오.';
      else if (기 >= 2) 분기판정 = '주의가 필요한 분기입니다. 방어에 집중하고 무리한 계획은 피하십시오.';
      else if (용 >= 1 && 기 === 0) 분기판정 = '전반적으로 순탄한 분기입니다. 기회를 놓치지 말고 움직이십시오.';
      else if (기 >= 1 && 용 === 0) 분기판정 = '조심스러운 분기입니다. 현상 유지에 집중하십시오.';
      else 분기판정 = '기복이 있는 분기입니다. 월별 흐름에 따라 유연하게 대응하십시오.';

      lines.push(`▸ ${q.이름}: ${등급요약}`);
      lines.push(`  ${분기판정}`);
    }

    // 최고/최악의 달
    const 용신달 = 월운풀이결과.filter(p => p.성격==='용신월');
    const 기신달 = 월운풀이결과.filter(p => p.성격==='기신월');

    lines.push('');
    if (용신달.length > 0) {
      lines.push(`★ 올해 가장 좋은 달: ${용신달.map(p=>p.월+'월').join(', ')}`);
      lines.push(`  이 달에 중요한 계약, 면접, 고백, 투자 등을 배치하십시오.`);
    }
    if (기신달.length > 0) {
      lines.push(`★ 올해 가장 조심할 달: ${기신달.map(p=>p.월+'월').join(', ')}`);
      lines.push(`  이 달에는 큰 결정을 피하고, 건강 관리에 집중하십시오.`);
    }
  }

  // ── 핵심 조언 ──
  lines.push('');
  lines.push(`◎ ${기준년도}년 핵심 실천 가이드`);
  lines.push(`▸ 용신 강화: ${oh[용신]}(${용신}) — 색상, 방위, 음식, 직업 모두 이 오행을 강화하십시오.`);
  lines.push(`▸ 기신 주의: ${oh[기신]}(${기신}) — 이 오행이 강해지는 달에는 방어적으로 임하십시오.`);
  lines.push(`▸ 용신 색상을 일상에 배치하고, 용신 방위로 중요한 약속을 잡으십시오.`);
  lines.push(`▸ 월별 운세를 참고하여 "공격할 달"과 "지킬 달"을 미리 구분해두십시오.`);

  return lines.join('\n');
}

