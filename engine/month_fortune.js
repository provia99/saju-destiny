// ── 합충 특수 이벤트 ──
function 합충해석생성(월지지, 원국) {
  const parts = [];
  const 충맵 = {子:'午',午:'子',丑:'未',未:'丑',寅:'申',申:'寅',卯:'酉',酉:'卯',辰:'戌',戌:'辰',巳:'亥',亥:'巳'};
  const 합맵 = {子:'丑',丑:'子',寅:'亥',亥:'寅',卯:'戌',戌:'卯',辰:'酉',酉:'辰',巳:'申',申:'巳',午:'未',未:'午'};
  const 위치 = ['년지','월지','일지','시지'];
  const 영역맵 = {년지:'조상·사회 영역',월지:'직업·형제 영역',일지:'배우자·내면 영역',시지:'자녀·말년 영역'};
  const 지지들 = [원국.년주.지지, 원국.월주.지지, 원국.일주.지지, 원국.시주.지지];
  for (let i = 0; i < 지지들.length; i++) {
    if (충맵[월지지] === 지지들[i])
      parts.push(`⚡ 이 달 월지(${월지지})가 원국 ${위치[i]}(${지지들[i]})와 충(沖)합니다. ${영역맵[위치[i]]}에서 변동이 생길 수 있으니 큰 결정은 신중하게 내리십시오.`);
    if (합맵[월지지] === 지지들[i])
      parts.push(`🤝 이 달 월지(${월지지})가 원국 ${위치[i]}(${지지들[i]})와 합(合)합니다. ${위치[i]} 영역에서 좋은 인연이나 기회가 생깁니다.`);
  }
  return parts;
}

// ── 대운 맥락 ──
function 대운맥락(현재대운, 월성격) {
  if (!현재대운) return '';
  const 길흉 = 현재대운.대운길흉 || 현재대운.길흉 || '';
  if (/용신/.test(길흉) && 월성격 === '용신월')
    return '대운과 월운이 모두 용신 방향입니다. 10년 주기와 월 주기가 동시에 힘을 실어주는 최고의 타이밍입니다. 이런 기회는 흔치 않습니다.';
  if (/용신/.test(길흉) && 월성격 === '기신월')
    return '대운은 좋지만 이번 달은 기신월입니다. 큰 흐름은 순탄하니 이 달만 잘 넘기면 됩니다. 잠시 멈추되 방향은 유지하십시오.';
  if (/기신/.test(길흉) && 월성격 === '용신월')
    return '대운이 기신이지만 이번 달만큼은 용신의 힘이 작용합니다. 이 달에 최대한 성과를 내십시오. 이 기회를 놓치면 다음 용신월까지 기다려야 합니다.';
  if (/기신/.test(길흉) && 월성격 === '기신월')
    return '대운과 월운 모두 기신 방향입니다. 올해 가장 힘든 시기입니다. 건강과 안전을 최우선으로 하고, 큰 결정은 절대 피하십시오.';
  return '';
}

// ── 십성 영역별 텍스트 가져오기 ──
function 십성텍스트(십성명, 월성격) {
  const s = 십성영역[십성명];
  if (!s) return null;
  const 등급 = (월성격 === '용신월' || 월성격 === '희신월') ? '길' : 월성격 === '기신월' ? '흉' : '중';
  const data = s[등급];
  if (typeof data === 'string') return { 종합: data }; // 이전 형식 호환
  return data; // 새 형식: {종합, 재물, 직업, 건강, 인간관계}
}

