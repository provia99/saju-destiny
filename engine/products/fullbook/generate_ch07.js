function _normalize신강약(raw) {
  if (!raw) return '신약(身弱)';
  if (/강신약/.test(raw)) return '신약(身弱)';
  if (/약신강/.test(raw)) return '신강(身强)';
  if (/강신강/.test(raw)) return '신강(身强)';
  if (/약신약/.test(raw)) return '신약(身弱)';
  if (raw.includes('('))  return raw;
  return raw.includes('강') ? '신강(身强)' : '신약(身弱)';
}
