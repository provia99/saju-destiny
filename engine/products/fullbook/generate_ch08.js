function 대운원국_합충분석(d) {
  const 대지 = d.지지;
  const 합목록 = [];
  const 충목록 = [];
  const 위치명 = ['년지','월지','일지','시지'];

  for (let i=0; i<원국_지지목록.length; i++) {
    const 원지 = 원국_지지목록[i];
    if (!원지) continue;
    if ((_대운합관계[대지]||[]).includes(원지)) {
      합목록.push(`${위치명[i]}(${원지})와 합`);
    }
    if (_대운충관계[대지] === 원지) {
      충목록.push(`${위치명[i]}(${원지})와 충`);
    }
  }

  const 결과목록 = [...합목록, ...충목록];
  return 결과목록.length > 0 ? 결과목록.join(', ') : '';
}

