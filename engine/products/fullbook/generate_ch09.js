// ── 달력 기반 상세 일별 분석 (generate_운세달력.js 로직 재사용) ──
function 손없는날판정(day) { const d = day % 10; return d === 9 || d === 0; }

function 일별점수계산(ij, _용신, _희신, _기신, _일간) {
  const tgOh = to맵[ij.천간], jjOh = jo맵[ij.지지], ilganOh = to맵[_일간];
  let score = 50;
  if (tgOh === _용신) score += 20; else if (tgOh === _희신) score += 12; else if (tgOh === _기신) score -= 15;
  if (jjOh === _용신) score += 18; else if (jjOh === _희신) score += 10; else if (jjOh === _기신) score -= 12;
  const 상생맵 = {木:'火',火:'土',土:'金',金:'水',水:'木'};
  const 상극맵 = {木:'土',土:'水',水:'火',火:'金',金:'木'};
  if (상생맵[ilganOh] === tgOh) score += 4;
  if (상생맵[tgOh] === ilganOh) score += 6;
  if (상극맵[tgOh] === ilganOh) score -= 5;
  if (상극맵[ilganOh] === tgOh) score -= 3;
  const ilganYang = 양간.includes(_일간), tgYang = 양간.includes(ij.천간);
  if (ilganYang === tgYang) score += 3; else score -= 2;
  const 합쌍 = {甲:'己',己:'甲',乙:'庚',庚:'乙',丙:'辛',辛:'丙',丁:'壬',壬:'丁',戊:'癸',癸:'戊'};
  if (합쌍[_일간] === ij.천간) score += 8;
  const 충쌍 = {甲:'庚',庚:'甲',乙:'辛',辛:'乙',丙:'壬',壬:'丙',丁:'癸',癸:'丁'};
  if (충쌍[_일간] === ij.천간) score -= 7;
  const jjIdx = 지지순.indexOf(ij.지지);
  const variation = [0, -2, 3, 1, -1, 2, 0, -3, 1, -2, 2, -1];
  score += variation[jjIdx] || 0;
  return Math.max(10, Math.min(100, score));
}

function 날짜분류(년, 월수) {
  const 말일 = 월말일(년, 월수);
  const 용신일=[], 희신일=[], 기신일=[], 중립일=[];
  // 달력 기반 상세 분석 데이터
  // 기신은 상위 스코프(30줄)에서 이미 선언됨
  const 육합맵 = {子:'丑',丑:'子',寅:'亥',亥:'寅',卯:'戌',戌:'卯',辰:'酉',酉:'辰',巳:'申',申:'巳',午:'未',未:'午'};
  const 충맵 = {子:'午',午:'子',丑:'未',未:'丑',寅:'申',申:'寅',卯:'酉',酉:'卯',辰:'戌',戌:'辰',巳:'亥',亥:'巳'};
  const 합쌍 = {甲:'己',己:'甲',乙:'庚',庚:'乙',丙:'辛',辛:'丙',丁:'壬',壬:'丁',戊:'癸',癸:'戊'};

  const 대길일목록=[], 대주의일목록=[], 흉일목록=[], 손없는날목록=[];
  const 역마일목록=[], 도화일목록=[], 충일목록=[], 합일목록=[];
  let 길일수=0, 흉일수=0;
  let 최고점수=0, 최저점수=100, 최고점수일=null, 최저점수일=null;

  for(let d=1; d<=말일; d++) {
    const j = 일진(년, 월수, d);
    const s = 성격calc(j.to, j.jo);
    const 항목 = {d, 표기:j.표기};
    if(s==='용신') 용신일.push(항목);
    else if(s==='희신') 희신일.push(항목);
    else if(s==='기신') 기신일.push(항목);
    else 중립일.push(항목);

    // 상세 분석
    const tgOh = j.to, jjOh = j.jo;
    const isLucky = tgOh === 용신 || jjOh === 용신;
    const isCaution = tgOh === 기신 && jjOh === 기신;
    const is대길 = (tgOh === 용신 && jjOh === 희신) || (tgOh === 희신 && jjOh === 용신);
    const is충 = 충맵[일지] === j.지지;
    const is대주의 = isCaution && is충;
    const is역마 = ['寅','申','巳','亥'].includes(j.지지);
    const is도화 = ['子','午','卯','酉'].includes(j.지지);
    const is천간합 = 합쌍[일간] === j.천간;
    const is육합 = 육합맵[일지] === j.지지;
    const sonDay = 손없는날판정(d);

    // 점수 계산
    const score = 일별점수계산(j, 용신, 희신, 기신, 일간);
    if(score > 최고점수) { 최고점수 = score; 최고점수일 = {d, 표기:j.표기, score}; }
    if(score < 최저점수) { 최저점수 = score; 최저점수일 = {d, 표기:j.표기, score}; }

    // 길일/흉일 카운트
    if(isLucky || is대길) 길일수++;
    if(isCaution || is대주의) 흉일수++;

    // 태그 수집
    if(is대길) 대길일목록.push({d, 표기:j.표기});
    if(is대주의) 대주의일목록.push({d, 표기:j.표기});
    else if(isCaution) 흉일목록.push({d, 표기:j.표기});
    if(sonDay) 손없는날목록.push(d);
    if(is역마) 역마일목록.push(d);
    if(is도화) 도화일목록.push(d);
    if(is충) 충일목록.push({d, 표기:j.표기});
    if(is천간합 || is육합) 합일목록.push({d, 표기:j.표기});
  }

  // 달력요약 생성
  const 달력요약 = {
    길일수,
    흉일수,
    용신일비율: 말일 > 0 ? Math.round((용신일.length / 말일) * 100) : 0,
    대길일목록,    // 🌟대길일
    대주의일목록,  // ⚠️대주의
    흉일목록,      // ⚠️흉일
    손없는날목록,  // 손없는날 (이사/행사 좋은 날)
    역마일목록,    // 역마일 (여행/이동 좋은 날)
    도화일목록,    // 도화일 (인연/미용 좋은 날)
    충일목록,      // 충일 (변화 주의)
    합일목록,      // 합일 (만남/협력 좋은 날)
    최고점수일,    // 점수 기준 최고일
    최저점수일,    // 점수 기준 최저일
  };

  return {용신일, 희신일, 기신일, 중립일, 말일, 달력요약};
}

