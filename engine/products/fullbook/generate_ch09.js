// ── 십성/성격/대운성격 헬퍼 ──
function 십성calc(일간, 대상) {
  const 일o=to맵[일간], 대o=to맵[대상];
  const 같음=양간.includes(일간)===양간.includes(대상);
  const 아생={木:'火',火:'土',土:'金',金:'水',水:'木'};
  const 아극={木:'土',火:'金',土:'水',金:'木',水:'火'};
  const 생아={木:'水',火:'木',土:'火',金:'土',水:'金'};
  const 극아={木:'金',火:'水',土:'木',金:'火',水:'土'};
  if(대o===일o) return 같음?'비견':'겁재';
  if(아생[일o]===대o) return 같음?'식신':'상관';
  if(아극[일o]===대o) return 같음?'편재':'정재';
  if(생아[일o]===대o) return 같음?'편인':'정인';
  if(극아[일o]===대o) return 같음?'편관':'정관';
  return '비견';
}

function 성격calc(to, jo) {
  if(to===용신||jo===용신) return '용신';
  if(to===희신||jo===희신) return '희신';
  if(to===기신||jo===기신) return '기신';
  return '중립';
}
function 세운성격calc(t,j) { return 성격calc(to맵[t],jo맵[j])+'세운'; }
