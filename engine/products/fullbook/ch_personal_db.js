const _fs_shared   = require('fs');
const _path_shared = require('path');

const SHARED_KEYS = [
  '기신오행','기신표기','기신오행키','기신오행한글','기신방위',
  '현재대운성격','현재대운간지','현재대운나이범위',
  '건강주의대운목록','현재대운건강위험','건강위험대운목록','평생건강주의계통',
  '현재대운성격단','현재대운십이운성','현재대운천간십성','현재대운지지십성',
];

function injectCh08Fields(M, samplesDir, slots) {
  const fileId  = M.id || M.이름 || '';
  const ch08Path = _path_shared.join(samplesDir, `${fileId}_ch08.json`);
  if (!_fs_shared.existsSync(ch08Path)) return;
  try {
    const ch08 = JSON.parse(_fs_shared.readFileSync(ch08Path, 'utf8'));
    for (const k of SHARED_KEYS) {
      if (ch08[k] !== undefined && ch08[k] !== null && ch08[k] !== '') {
        if (M[k]     === undefined) M[k]     = ch08[k];
        if (slots && slots[k] === undefined) slots[k] = String(ch08[k]);
      }
    }
  } catch(e) { /* ch08.json 없거나 파싱 오류면 무시 */ }
}

module.exports.injectCh08Fields = injectCh08Fields;
