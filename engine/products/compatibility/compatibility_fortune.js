// 일간 상성 조합 키 선택 (천간합/충 오버라이드 포함)
function pick상성키(관계) {
  const 합 = 관계.includes('천간합');
  const 충 = 관계.includes('천간충');
  const 생 = 관계.includes('상생');
  const 극 = 관계.includes('상극');
  const 화 = 관계.includes('비화');
  if (합 && 생) return '상생_천간합';
  if (합 && 극) return '상극_천간합';
  if (합 && 화) return '비화_천간합';
  if (충 && 극) return '상극_천간충';
  if (충 && 생) return '상생_천간충';
  if (합) return '천간합';
  if (충) return '천간충';
  if (생) return '상생';
  if (극) return '상극';
  return '비화';
}

// 동일 위치쌍 감지 (예: "A 일지(丑) ↔ B 일지(亥) 육합" → 일지 2회 등장)
function 동일위치(s, 위치) {
  const re = new RegExp(`${위치}\\([^)]+\\)`, 'g');
  return (s.match(re) || []).length === 2;
}

function bullets(arr) {
  return arr.length ? arr.map(s => `- ${s}`).join('\n') : '';
}

function dbBullets(lines) {
  return lines.map(s => `- ${s}`).join('\n\n');
}

